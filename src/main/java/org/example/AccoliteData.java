package org.example;

public class AccoliteData {
    private String date;
    private String month;
    private String team;
    private String panelName;
    private String round;
    private String Skill;
    private String time;
    private String Candidate_current_loc;
    private String Candidate_Pref_loc;
    private String Candidate_Name;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public String getPanelName() {
        return panelName;
    }

    public void setPanelName(String panelName) {
        this.panelName = panelName;
    }

    public String getRound() {
        return round;
    }

    public void setRound(String round) {
        this.round = round;
    }

    public String getSkill() {
        return Skill;
    }

    public void setSkill(String skill) {
        Skill = skill;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getCandidate_current_loc() {
        return Candidate_current_loc;
    }

    public void setCandidate_current_loc(String candidate_current_loc) {
        Candidate_current_loc = candidate_current_loc;
    }

    public String getCandidate_Pref_loc() {
        return Candidate_Pref_loc;
    }

    public void setCandidate_Pref_loc(String candidate_Pref_loc) {
        Candidate_Pref_loc = candidate_Pref_loc;
    }

    public String getCandidate_Name() {
        return Candidate_Name;
    }

    public void setCandidate_Name(String candidate_Name) {
        Candidate_Name = candidate_Name;
    }
}
